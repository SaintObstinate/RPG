    private void damageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_damageBtnActionPerformed

        damage = Integer.parseInt(JOptionPane.showInputDialog(this, "Damage:",1));
        if(damage<=0){
            JOptionPane.showMessageDialog(this, "Dano Incorreto", "Erro!", JOptionPane.WARNING_MESSAGE);
        }else{
            if(aux.rosa.getHpAtual()>0){
                if(damage <=aux.rosa.getHpAtual()){

                    hpBar.setValue(aux.rosa.getHpAtual()-damage);
                    aux.rosa.setHpAtual(aux.rosa.getHpAtual()-damage);
                    System.out.println(aux.rosa.getHpAtual());
                    hpBar.setString(Integer.toString(aux.rosa.getHpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getHP()));
                    mpBar.setString(Integer.toString(aux.rosa.getMpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getMP()));
                    System.out.println(aux.rosa.getMpAtual()+ "MP MP MP");
                }
                else{
                    JOptionPane.showMessageDialog(this, "HP Chegou a zero", "Aviso!", JOptionPane.WARNING_MESSAGE);
                    aux.rosa.setHpAtual(0);
                    hpBar.setValue(aux.getPersonagem(5).getHpAtual());
                    hpBar.setString(Integer.toString(aux.rosa.getHpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getHP()));

                }
            }
        }
    }//GEN-LAST:event_damageBtnActionPerformed

