    private void healBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_healBtnActionPerformed
        heal = Integer.parseInt(JOptionPane.showInputDialog(this, "Heal:",1));
        if(heal<=0){
            JOptionPane.showMessageDialog(this, "Cura Incorreta", "Erro!", JOptionPane.WARNING_MESSAGE);
        }else{
            if(aux.rosa.getHpAtual()<aux.rosa.getHP()){
                if(heal <= aux.getPersonagem(5).getHP()){
                    hpBar.setValue(aux.rosa.getHpAtual()+heal);
                    aux.rosa.setHpAtual(aux.rosa.getHpAtual()+heal);
                    System.out.println(aux.rosa.getHpAtual());
                    hpBar.setString(Integer.toString(aux.rosa.getHpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getHP()));
                    mpBar.setString(Integer.toString(aux.rosa.getMpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getMP()));
                }
                else{
                    JOptionPane.showMessageDialog(this, "HP cheio", "Aviso!", JOptionPane.WARNING_MESSAGE);
                    aux.rosa.setHpAtual(aux.rosa.getHP());
                    hpBar.setValue(aux.getPersonagem(5).getHP());
                    hpBar.setString(Integer.toString(aux.rosa.getHpAtual())+"/"+Integer.toString(aux.getPersonagem(5).getHP()));

                }
            }
        }
    }//GEN-LAST:event_healBtnActionPerformed

